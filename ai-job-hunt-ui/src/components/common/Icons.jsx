const iconDefaults = {
  width: 18,
  height: 18,
  viewBox: '0 0 24 24',
  fill: 'none',
  stroke: 'currentColor',
  strokeWidth: 1.75,
  strokeLinecap: 'round',
  strokeLinejoin: 'round',
};

function Icon({ children, size, className = '', style = {}, ...props }) {
  const merged = { ...iconDefaults, ...props };
  if (size) {
    merged.width = size;
    merged.height = size;
  }
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      className={`icon ${className}`}
      style={style}
      width={merged.width}
      height={merged.height}
      viewBox={merged.viewBox}
      fill={merged.fill}
      stroke={merged.stroke}
      strokeWidth={merged.strokeWidth}
      strokeLinecap={merged.strokeLinecap}
      strokeLinejoin={merged.strokeLinejoin}
    >
      {children}
    </svg>
  );
}

